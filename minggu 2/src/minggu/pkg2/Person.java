class Person {
    String name;
    int age;
    
    public Person(String name, int age){
        this.name = name;
        this.age = age;
    }
    
    public String getName() {
        return name;
    }
    public String getAge() {
        return name;
    }
    public void setAge(int age) {
        this.age = age;
    }
    
}
